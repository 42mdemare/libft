int	ft_atoi(const char *nptr)
{
	int	i;
	int	number;
	int	signe;

	i = 0;
	number = 0;
	signe = 1;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			signe = -signe;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
		number = number * 10 + (nptr[i++] - '0');
	return (number * signe);
}
